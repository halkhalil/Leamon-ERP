CREATE TABLE INVOICE_BILL(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
	PARTYINFOID					INTEGER,
	INVOICENUM					VARCHAR(100) DEFAULT NULL,
	INVOICEDATE					VARCHAR(100) DEFAULT NULL,
	ORDERBOOKBY					VARCHAR(100) DEFAULT NULL,
	TRANSPORT					VARCHAR(100) DEFAULT NULL,
	PACKETNUM					VARCHAR(100) DEFAULT NULL,
	GSTAMOUNT					VARCHAR(100) DEFAULT NULL,
	BILLAMOUNT					VARCHAR(100) DEFAULT NULL,		
	CREATEDDATE					TIMESTAMP,
	LASTUPDATEDDATE				TIMESTAMP,
	ISENABLE					BOOLEAN DEFAULT TRUE,
	CONSTRAINT	UNIQUE_INVOICE_NUM		UNIQUE(INVOICENUM)
);
