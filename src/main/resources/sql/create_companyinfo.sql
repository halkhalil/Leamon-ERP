DROP TABLE COMPANY_INFO;
CREATE TABLE COMPANY_INFO(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 100, INCREMENT BY 1) PRIMARY KEY,
	NAME			VARCHAR(100) NOT NULL,
	SUPPLIER		VARCHAR(100) DEFAULT NULL,
	ADDRESS			VARCHAR(1000) DEFAULT NULL,
	CITY			VARCHAR(100) DEFAULT NULL,
	STATE			VARCHAR(100) DEFAULT NULL,
	STATECODE		VARCHAR(50) DEFAULT NULL,
	GST_TIN_NUMBER	VARCHAR(100) DEFAULT NULL,
	BILLPREFIX		VARCHAR(100) DEFAULT NULL,
	PHONE			VARCHAR(100) DEFAULT NULL,
	BANKNAME		VARCHAR(100) DEFAULT NULL,
	BANKACCOUNT		VARCHAR(100) DEFAULT NULL,
	IFSC			VARCHAR(100) DEFAULT NULL,		
	BANKBRANCH		VARCHAR(100)  DEFAULT NULL,
	COUNTRY			VARCHAR(100) DEFAULT NULL,
	CREATEDDATE		TIMESTAMP,
	LASTUPDATED		TIMESTAMP,
	ISENABLE		BOOLEAN DEFAULT TRUE,
	CONSTRAINT	UNIQUE_COMPANY_INFO		UNIQUE(NAME)
);

